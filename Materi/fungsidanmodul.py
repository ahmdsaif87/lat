def penjumlahan(a,b) :
    hasil = a * b
    print(hasil)
penjumlahan(10,2)

def luas_persegi_panjang(panjang,lebar):
    return panjang * lebar
print(luas_persegi_panjang(8,5))

def nilai(a,b,c,d):
    hasil1 = a * c
    hasil2 = b - d
    return hasil1,hasil2
print(nilai(10,20,30,40))

